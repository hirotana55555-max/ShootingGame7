/**
 * @file /app/page.tsx
 * @description このプロジェクトのメインページを定義するReactコンポーネントです。
 *              Next.jsのルーティングシステムにより、ユーザーがサイトのトップページに
 *              アクセスした際にこのコンポーネントが描画されます。
 *              いわば、Webアプリケーション全体の「玄関」の役割を果たします。
 */

// 依存関係のインポート
// -----------------------------------------------------------------------------
// '@' はプロジェクトのルートディレクトリを指すエイリアス（別名）です。
// ここでは、'components' フォルダの中にある 'GameCanvas.tsx' ファイルから、
// GameCanvasコンポーネントをインポートしています。
// GameCanvasコンポーネントが、実際のゲーム画面を描画する心臓部となります。
import GameCanvas from '@/components/GameCanvas';

/**
 * Homeコンポーネント
 * @description サイトのホームページ（ルートURL: /）に表示される内容を定義します。
 * @returns {JSX.Element} レンダリングされるReact要素。
 */
export default function Home() {
  // return (...) の中身が、実際にブラウザに表示されるHTMLの構造となります。
  // JSXという、HTMLに似た記法で記述されています。
  return (
    // <main> は、このページの主要なコンテンツを囲むためのHTMLタグです。
    <main>
      {/* <h1> は、最も重要な見出しを表すHTMLタグです。
          ここではゲームのタイトル "ShootingGame7" を表示しています。 */}
      <h1>ShootingGame7</h1>

      {/* <GameCanvas /> は、先ほどインポートしたGameCanvasコンポーネントを
          実際にページ上に配置（レンダリング）する記述です。
          この一行により、ゲーム画面がこの場所に描画されます。
          このように、Reactではコンポーネントを部品のように組み合わせてUIを構築します。 */}
      <GameCanvas />
    </main>
  );
}
